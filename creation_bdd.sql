CREATE TABLE RECETTE (
	id_recette		NUMBER(8)		NOT NULL,
	nom				VARCHAR2(128)	NOT NULL,
	description		VARCHAR2(1024),
	difficulte		NUMBER(1)		NOT NULL,
	prix			NUMBER(1)		NOT NULL,
	nb_pers			NUMBER(16)		NOT NULL,
	id_utilisateur	NUMBER(8)		NOT NULL,
	id_media		NUMBER(8)		NOT NULL,
	PRIMARY KEY (id_recette),
--	FOREIGN KEY (id_auteur) REFERENCES UTILISATEUR (id_utilisateur),
--	FOREIGN KEY (id_media) REFERENCES MEDIA(id_media),
	CONSTRAINT ck_difficulte CHECK (difficulte BETWEEN 1 AND 5),
	CONSTRAINT ck_prix CHECK (prix BETWEEN 1 AND 5)
);

CREATE TABLE INGREDIENT (
	id_ingredient	NUMBER(8)		NOT NULL,
	nom				VARCHAR2(64)	NOT NULL,
	unite			VARCHAR2(32)    NOT NULL,
	calories		NUMBER(8,4),
	lipides			NUMBER(8,4),
	glucides		NUMBER(8,4),
	protides		NUMBER(8,4),
	id_media		NUMBER(8),
	PRIMARY KEY (id_ingredient)
--	FOREIGN KEY (id_media) REFERENCES MEDIA(id_media)
);

